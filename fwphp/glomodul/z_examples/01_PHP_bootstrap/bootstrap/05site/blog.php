<h2>Biography last 5 years</h2>
<p>Van Gogh lost his father to stroke in March of 1885. Although deeply stricken with grief, Van Gogh continued with his art, creating his first famous piece <b>The Potato Eaters</b>, for an interested group in Paris. Late that year, in November, Van Gogh moved to Antwerpen where he lived above a paint shop and ate very little. He continued his academic art studies at the Ecole des Beaux-Arts before moving to Paris in march of 1886.

<p>During two years in Paris Vincent Van Gogh overcame conflicts with his brother Theo and eventually met and befriended various artists including Paul Gauguin. He painted over 200 oil paintings during his time in Paris and left the city in February of 1888 to live in Arles (south France) where he eventually settled in the Yellow House. Van Gogh invited Gauguin to visit and, after some persuasion (6 monts), the artist eventually arrived. After two months of painting and talking, Van Gogh and Gauguin began disagreeing and eventually argued openly about their opposing artistic views. Van Gogh stalked Gauguin, eventually cutting off his own ear and handing it to a prostitute for safe keeping.

<p>After thirty townspeople of Arles protested Van Gogh's residency, he committed himself to the clinic at Auvers-sur-Oise in May of 1889. He made peace with his brother Theo and they worked with Dr. Theophile Peyron to help Van Gogh recover. He left the clinic in May of 1890 to live closer to his brother but managed to fall deeper into depression. On July 27 Van Gogh walked into a field and shot himself. Two days later he died. He is buried at the cemetery of Auvers-sur-Oise. (<a href="www.artquotes.net">www.artquotes.net</a>)</p>
                </p>
                

