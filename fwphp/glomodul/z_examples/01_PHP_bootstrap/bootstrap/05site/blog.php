<h2>Biography youth</h2>
<p>He moved to London in 1873 to work for another branch of the same firm, Messrs. Goupil & Co. Although he was earning more money than his father, Vincent van Gogh eventually became discouraged with the way he thought art was treated and he began conveying his opinions to clients. In 1876 he was terminated from his position.

<p>Van Gogh began focusing on religion instead and, after working at a boarding school in Ramsgate, he moved to Isleworth in Middlesex to relocate the school. The new position did not suit Vincent van Gogh and he began working as a minister's assistant with a nearby Methodist church. Soon afterwards, Van Gogh returned home and, after working a bookshop for a period of time, moved in with his uncle in order to attend the university in Amsterdam to study theology. He later left his uncle's home after failing in his studies and moved to Belgium to work as a missionary.

<p>After attempting to live in squalor in order to better understand those to whom he preached, Van Gogh was dismissed from the church and returned home to his parents once again. Van Gogh's father inquied about his son's sanity and wanted Van Gogh committed to the lunatic asylum in Geel. Van Gogh fled to Cuesmes where he started recording his observations in drawings. Although he had drawn in the past, Van Gogh focused more on his talent as the urgency of his brother Theo.

<p>In 1880 Vincent van Gogh traveled to Brussels to study under Willem Roelofs. The Dutch artist persuaded Van Gogh to study at the Royal Academy of Art where he learned the basics of form and anatomy. One year later Van Gogh returned to his parents and fell in love with his cousin. Despite his cousin Kee's refusal of marriage and his uncle's disgust, Van Gogh continued to pursue her, eventually causing his uncle to refuse any proposal. Van Gogh was openly distraught and settled in The Hague shortly thereafter.

<p>While in The Hague Van Gogh befriended and fell in love with a known prostitue named Clasina Maria Hoornik who had one child previous to meeting Van Gogh and one child while posing as his model. Although she later claimed this second child to belong to Van Gogh, the child's birth makes it unlikely to have Van Gogh as a father.

<p>After living The Hague for two years, Van Gogh moved back to the Netherlands to be near his parents. He fell in love with a neighbor's daughter, Margot Begemann, and they exchanged their mutual affections and desires for marriage. Their families disagreed and Margot attempted suicide. Although she survived, they did not marry.

