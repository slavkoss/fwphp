  <li>Poisoned pawn or piece. Famous line in Garry's beloved <strong>Najdorf Sicilian 
  Defense</strong> called the Poisoned Pawn Variation, was popularized by American World 
  Champion Bobby Fischer. <br /></li>
  <li>Pin&nbsp; - close cousin of skewer, is binding&nbsp; not as deadly as skewers, but 
  they are much more common - paralyzing pieces - especially in the endgame, 
  when fewer pieces are around to come to the aid of the pinned piece.&nbsp; Absolute 
  pin is when the piece is <strong>pinned against the king, so it cannot move</strong>. 
  Relative pins, since the piece can legally move. Garry points out that 
  relative pins can occasionally provide the opportunity for a surprising 
  counterattack by moving the pinned piece to create an even stronger threat.
  <br /></li>
  <li>Discovered attack and double check, its deadliest form.<br /></li>
  <li>Deflection/Attraction - famous 1967 problem by Soviet-Russian composer 
  Leopold Mitrofanov, Mitrofanov's Deflection later corrected.<br /></li>
  <li>Interference is a category of deflection, but it's also based on our 
  ability to use opponent's pieces to create obstacles. Don't take winning queen 
  versus rook for granted! <br /></li>
  <li>Overload with too much work - when one piece has two jobs, like defending 
  two pieces. Often to take advantage of this, you need to choose the right move 
  order. If possible, force the overloaded piece to abandon one of its 
  responsibilities.<br /></li>
  <li>Trading pieces - as with pawn moves, captures cannot be undone,&nbsp; consider 
  what you are gaining and what you are giving up.<br /></li>
</ol>
