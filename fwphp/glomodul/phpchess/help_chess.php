<h1>Openings</h1>
<p><strong>
Opening principles&nbsp;
</strong><br />1. protect king<br />2. controlling the center<br />3. develop 
pieces (active pieces)<br />4. oponent **threats**</p>
<p>Studying the history of the openings just to know all the nuances that 
  could help you to avoid traps or collapsing if your opponent throws&nbsp; new idea 
  at you.&nbsp; Word sharp to describe an opening, it doesn't always have the same 
  life-or-death connotation as a sharp tactical middlegame position. <strong>Sharp 
  openings</strong> are concrete, subject to rigorous analysis and preparation to find 
  the very best moves at every point. If you don't have the time to study, sharp 
  openings like the <strong>Sicilian and the Grunfeld</strong> aren't for you!&nbsp; Goddess of chess, 
  Caissa, is watching over&nbsp; players and punishing those who betray her 
  traditions and precepts! <br />
<br />
You have to start with gambits - Evans Gambit, aggressive opening Kasparov 
  used against his opponent, Viswanathan Anand&nbsp; Tiger of Madras India 1995 Riga, 
  Latvia.&nbsp; </p>
<p>New major openings are rare.&nbsp; 
Perhaps the last major gambit opening was Benko's Gambit (1.d4 Nf6 2.c4 c5 3.d5 
b5), which Paul Benko introduced in the late 1960s and heavily analyzed in the 
1970s.&nbsp; At first, Black was winning most of the game in master play, but 
with time, it has been neutralized and is now rare among top grandmasters.<br />
The older chess openings tend to be named for geographic places and people. Many 
openings are named after nationalities, for example Indian (1.d4 Nf6), English 
(1.c4), Spanish or Ruy Lopez (1.e4 e5 2.Nf3 Nc6 3.Bb5), French (1.e4 e6), Dutch 
(1.d4 f5), Scotch (1.e4 e5 2.Nf3 Nc6 3.d4), Russian (1.e4 e5 2.Nf3 Nf6), Italian 
(1.e4 e5 2.Nf3 Nc6 3.Nc4 Bc5), Scandinavian (1.e4 d5), and Sicilian (1.e4 c5). 
Cities are also used, such as Vienna (1.e4 e5 2.Nc3), Berlin variation if the 
Ruy Lopez (1.e4 e5 2.Nf3 Nc6 3.Bb5 Nf6), and Wilkes-Barre (1.e4 e5 2.Nf3 Nc6 
3.Bc4 Nf6 4.Ng5 Bc5). <a href="https://www.youtube.com/watch?v=eebnBP5XFVc">
https://www.youtube.com/watch?v=eebnBP5XFVc</a>
