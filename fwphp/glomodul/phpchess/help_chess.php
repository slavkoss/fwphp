<h2>1. Pre-Morphy period ~350 years 1510-1850<br /></h2>
<p>Renaissance. By 1510 the old type of chess was obsolete in most of Italy and 
Spain. Analysis was the ruling motive in the literature of the period. Openings 
known today as the Ruy Lopez, Giuoco Piano, Petroff defense, Philidor Defense, 
Bishop's Opening and Queen's Gambit Accepted, were first outlined in a late 15th 
century manuscript (in the Gottingen University Library). Ruy Lopez (from Philip 
II court) first appears in 1559 when this Spanish priest visited Italy and 
defeated all the Roman players. After Greco's death in 1634, Italy produced no 
outstanding players for over a hundred years.&nbsp; 1669, a French translation 
of his collection of games&nbsp; was published in Paris.<br /></p>
<p>18th century coffee-houses of London and Paris were the leading centers of 
chess activity ( Cafe de la Regence in Paris and Slaughter's Coffee House in 
London).&nbsp; Andre D. Philidor was musician (1775 till death in 1795 he spent 
spring of each year in London and the rest of the year in Paris). He was the 
first to define and explain the principles of chess strategy and tactics.<br /></p>
<p>First half of the 19th century Captain W. D. Evans discovered his gambit in 
1824,&nbsp; William Lewis,&nbsp; great Howard Staunton from 1843 best. In France 
Alexander Deschapelles; Pierre de Saint-Amant ("French Defense).&nbsp; Central 
Europe&nbsp; B. Horwitz,&nbsp; Russian Petroff (Petrov), Livonian, Kieseritzky.<br />
</p>
<p>From a purely technical point of view games of 350 years to 19th century are 
not of vital importance but&nbsp;one wonders whether all gains (theory, knowledge, 
systematic analysis ) compensate for&nbsp; spirit of freshness, of eternal 
adventure, of naivete, charm, their sociable and leisurely character.<br /></p>
<p>Romantic Era of chess in the second half of the 19th century was all about 
gambits, sacrifices, open lines, and active pieces. Attack at all costs! Defense 
is for cowards.&nbsp; Fell into decline as its failures mounted against the more 
pragmatic and dogmatic teachings of Wilhelm Steinitz and Siegbert Tarrasch in 
the late 19th&nbsp; and early 20th. <br /></p>
<h2>2. Morphy period 1857 - 1880 <br /></h2>
<p>1851 new age :&nbsp; first International Chess Tournament in London, Adolph 
Anderssen of Berlin won - Another great player, almost as great as Morphy, and 
in the opinion of some capable judges even superior to him. Anderssen was best 
about ten years before Morphy's appearance. Also brilliancy and accuracy of 
combinations. Anderssen died of a heart attack at the age of 60.<br /></p>
<p>Paul Morphy spoiled rich child, greatest natural chess talent. 1858 defeated 
Anderssen and many other Europeans and retited when was 22 years old (declined 
to play or discuss chess). <strong>Brilliancy and accuracy of combinations, radical 
innovator</strong>. It was he who introduced the innovation which proved to be a 
<strong>death-knell (smrtno zvono) of&nbsp;romantic</strong> type of chess in which brilliancy was the be-all and 
end-all of every game. Correctness of his moves instead <strong>useless sacrifices</strong>. 
Intuitive with the logical as only the great artist can.&nbsp; Note how quickly 
Morphy made <strong>converts</strong>. <br /></p>
<p>Many report that he was able to recite from memory nearly the entire Civil 
Code of Louisiana (3,500 articles). Fischer&nbsp; and Viswanathan Anand ranked 
Morphy among the ten greatest chess players of all time. Fischer described him 
as "perhaps the most accurate player who ever lived". Morphy learned to play 
chess by watching games between his father and uncle, at twelve years of age 
defeated visiting Hungarian master Johann Loewenthal in a three game match. 
Morphy was a player who intuitively knew what was best, and in this regard he 
has been likened to Jose Capablanca. He was, like Capablanca, a child prodigy; 
he played quickly, would draw or even win games despite getting into bad 
positions. Anderssen said that, after one bad move against Morphy, one might as 
well resign. Garry Kasparov said that Morphy realized quarter-century before 
Wilhelm Steinitz had formulated principles :<br /></p>
<ol>
  <li>fast development of the pieces<br /></li>
  <li>domain of the centre<br /></li>
  <li>opening lines <br /></li>
</ol>
<p>Reuben Fine : "[Morphy's] glorifiers went on to urge that he was the most 
brilliant genius who had ever appeared But if we examine Morphy's record and 
games critically, we cannot justify such extravaganza. He was so far ahead of 
his rivals that it is hard to find really outstanding examples of his skill."
<br /></p>
<p>Morphy was called "The Pride and Sorrow of Chess". Born to a wealthy and 
distinguished (ugledna) family in New Orleans 1837,&nbsp; died on July 10. 1884 
at the age of 47 by stroke brought on by entering cold water in his bathtub 
after a long walk in the midday heat. The Morphy mansion 417 Royal Street which 
backs up to Bourbon Street, sold by the family in 1891, became the site of the 
well-known restaurant Brennan's.&nbsp; In accord with the prevailing sentiment 
of the time, Morphy esteemed chess only as an amateur activity, considering the 
game unworthy of pursuit as a serious occupation. Chess professionals were 
viewed in the same light as professional gamblers.&nbsp; Morphy was never able 
to establish a successful law practice (1861 outbreak of the American Civil War, 
suffered from delusions of persecution, chasing, progon). He lived a life of 
idleness, living off his family's fortune, was cared by his sister and mother.<br />
</p>
<h2>3. Age of Steinitz 10 years till 1890<br /></h2>
<p>Steinitz was genius accused "destroyed brilliancy in chess" - not true. Steinitz 
the first master of positional chess, was a strikingly (impressing, hitting, 
prominent) brilliant player, not only as a mettlesome (spirited, proud and 
unbroken spirit) youngster, but even as a feeble (lacking in&nbsp;force, 
effectiveness, strength, vigour, strength, vitality, dynamism, energy) old man.</p>
<p>L1: Game No. 73 shows us how Steinitz played at the beginning of his career -&nbsp; 
enthusiastic disciple of the attacking school. Very shortly thereafter he 
experienced a thoroughgoing conversion. He became obsessed with the 
deeply-rooted carelessness, flashiness (exhibitionism, immodesty, tasteless 
showiness) and frequent unsoundness of attacking school oposed to&nbsp; 
combinations of Morphy, with their :<br /></p>
<ol>
  <li>natural development<br /></li>
  <li>logical preparation<br /></li>
  <li>accurate execution<br /></li>
</ol>
<p>A pervasive interest in the <strong>defense</strong> became his life-time passion (desired 
intensely); he was fascinated by the idea of refuting (prove to be false or 
incorrect)&nbsp; an unsound attack, of demonstrating to the opponent that one 
cannot lightly toss away pawns, or worse pieces, without retribution, that 
hit-or-miss and helter-skelter attacks should not be permitted to achieve their 
goal. All have absorbed fundamentals of his theories,&nbsp; whether they have 
agreed with him or agreed to disagree also <strong>poets of the chessboard as Zukertort, 
Tchigorin and Blackburne</strong> - their attacking play was purified and raised to 
finer artistic levels by Steinitz's probing and fruitful <strong>criticism</strong>.</p>
<p>In the classical period (say, from Steinitz till 1935 Nimzowitsch's death) 
most players were <strong>technicians</strong>, with <strong>Capablanca</strong> 
as the most famous example. <br /></p>
<h2>4. Modern Chess 15 years till 1905<br /></h2>
<p>Era of what is called, occasionally in rather a disdainful (insulting) tone, 
