<h3>Pgnscid</h3>
<p>&nbsp;is the separate program that you need to use to convert PGN (portable 
game notation) files into Scid DB. To convert a file named myfile.pgn, type:
<br />
<strong>c:<br />
cd "C:\Scid vs PC-4.21\bin"<br />
pgnscid.exe "<a href="file:///J:/awww/www/fwphp/glomodul/phpchess/games/book_1961_Koblenz_theory_practice/1961_Koblenz_theory_and_practice_from_chess.pgn">J:\awww\www\fwphp\glomodul\phpchess\games\book_1961_Koblenz_theory_practice\1961_Koblenz_theory_and_practice_from_chess.pgn</a>"<br />
(339 kB)<br />
</strong>and the scid DB (consisting of myfile.si3, myfile.sg3 and myfile.sn3) 
will be created. Any errors or warnings will be written to the file myfile.err.
<br />
Database `J:\awww\...\1961_Koblenz_theory_and_practice_from_chess' : 853 games, 
576 players, 678 events, 4 sites.<br />
There were no warnings or errors. Created are :<br />
1961_Koblenz_theory_and_practice_from_chess.sg4&nbsp; 72 kB<br />
1961_Koblenz_theory_and_practice_from_chess.si4&nbsp;&nbsp; 40 kB<br />
1961_Koblenz_theory_and_practice_from_chess.sn4&nbsp; 16 kB<br />
<br />
If you want DB to be created in a different directory or have a different name, 
you can add the database name to the command line, for example: pgnscid 
myfile.pgn mybase will create a database consisting of the files mybase.si3, 
mybase.sg3 and&nbsp; mybase.sn3. <br />
<br />
Note that <strong>pgnscid (and scid) can read Gzipped PGN files</strong> (e.g. 
mybase.pgn.gz) directly, so if you have a large PGN file compressed with Gzip to 
save disk space, you do not have to un-gzip it first. <br />
<br />
Options<br />
There are two optional arguments pgnscid can accept before the filename: -f and 
-x. <br />
The -f option forces overwriting of an existing database; by default, pgnscid 
will not convert to a database that already exists. <br />
The -x option causes pgnscid to ignore all text between games. By default, text 
between games is stored as a pre-game comment of the game that follows. This 
option only affects text between games; standard comments inside each game are 
still converted and stored. <br />
<br />
Formatting player names<br />
To reduce the number of multiple spellings of names that refer to the same 
player, some basic formatting of player names is done by pgnscid. For example, 
the number of spaces after each comma is standardized to one, any spaces at the 
start and end of a name are removed, and a dot at the end of a name is removed. 
Dutch prefixes such as "van den" and "Van Der" are also normalized to have a 
capital V and small d. <br />
<br />
You can edit (and even spellcheck) player, event, site and round names in Scid; 
see the Maintenance help page for details. </p>
