<h1>1. Chess positions<br /></h1>
<p>The best first chess moves for White are: 1.e4, 1.d4, 1.Nf3, 1.c4. Forget the 
rest, they are dubious. 1.d4 : you can play a quiet positional game most of the 
time and have a slight long-lasting advantage if you play correctly. Second 
advantage is that you cut your workload into half because you don't have to 
